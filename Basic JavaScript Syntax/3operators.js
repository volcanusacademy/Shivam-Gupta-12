// Arithmetic Operators
let addition = 5 + 3; // addition is 8
let subtraction = 10 - 4; // subtraction is 6
let multiplication = 6 * 7; // multiplication is 42
let division = 20 / 4; // division is 5
let modulus = 17 % 3; // modulus is 2

// Comparison Operators
let isEqual = 5 == '5'; // isEqual is true (value comparison)
let isStrictlyEqual = 5 === '5'; // isStrictlyEqual is false (data type mismatch)
let isNotEqual = 10 != '10'; // isNotEqual is false (value comparison)
let isStrictlyNotEqual = 10 !== '10'; // isStrictlyNotEqual is true (data type mismatch)
let isLessThan = 7 < 10; // isLessThan is true
let isGreaterThan = 15 > 12; // isGreaterThan is true
let isLessOrEqual = 20 <= 20; // isLessOrEqual is true
let isGreaterOrEqual = 25 >= 30; // isGreaterOrEqual is false

// Display the results using document.write
document.write("Addition: " + addition + "<br>");
document.write("Subtraction: " + subtraction + "<br>");
document.write("Multiplication: " + multiplication + "<br>");
document.write("Division: " + division + "<br>");
document.write("Modulus: " + modulus + "<br>");
document.write("Equality (==): " + isEqual + "<br>");
document.write("Strict Equality (===): " + isStrictlyEqual + "<br>");
document.write("Inequality (!=): " + isNotEqual + "<br>");
document.write("Strict Inequality (!==): " + isStrictlyNotEqual + "<br>");
document.write("Less Than (<): " + isLessThan + "<br>");
document.write("Greater Than (>): " + isGreaterThan + "<br>");
document.write("Less Than or Equal (<=): " + isLessOrEqual + "<br>");
document.write("Greater Than or Equal (>=): " + isGreaterOrEqual + "<br>");
